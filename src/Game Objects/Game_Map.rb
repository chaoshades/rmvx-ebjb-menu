#------------------------------------------------------------------------------
#  Function aliases for custom menu
#===============================================================================

class Game_Map
  
  #//////////////////////////////////////////////////////////////////////////
  # * Constructors
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Alias initialize
  #--------------------------------------------------------------------------
  alias initialize_ebjb initialize unless $@
  def initialize
    initialize_ebjb
    @map_infos = load_data("Data/MapInfos.rvdata")
  end
  
  #//////////////////////////////////////////////////////////////////////////
  # * Properties
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Get map name
  #--------------------------------------------------------------------------
  # GET
  def name
    return @map_infos[@map_id].name
  end
  
end
